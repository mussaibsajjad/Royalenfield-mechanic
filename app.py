# app.py

import streamlit as st
from rag import rag_answer   # your helper function

# â”€â”€â”€ Sidebar: tweak number of chunks â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
st.sidebar.header("Settings")
k = st.sidebar.slider("Context chunks (k)", min_value=1, max_value=10, value=4)

# â”€â”€â”€ Session state for history â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
if "history" not in st.session_state:
    st.session_state.history = []  # list of (role, message)

# â”€â”€â”€ Page header & input â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
st.title("ðŸ›  Royal Enfield Mechanics Assistant")
query = st.text_input("Ask your question:")

if st.button("Send") and query:
    # add user question
    st.session_state.history.append(("You", query))
    # get the answer
    with st.spinner("Thinking..."):
        answer = rag_answer(query, k=k)
    # add assistant reply
    st.session_state.history.append(("Assistant", answer))
    # clear input box
    st.session_state.query = ""

# â”€â”€â”€ Display the conversation â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
for role, msg in st.session_state.history:
    if role == "You":
        st.markdown(f"**You:** {msg}")
    else:
        st.markdown(f"**Assistant:** {msg}")

